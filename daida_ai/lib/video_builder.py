@dataclass(frozen=True)
class VideoInfo:
    """ffprobeで取得した動画情報。"""

    duration: float  # 秒
    width: int
    height: int
    video_codec: str
    audio_codec: str | None  # 音声なしの場合None
    audio_sample_rate: int | None
    file_size: int  # バイト
    nb_streams: int


def probe_video(video_path: Path) -> VideoInfo:
    """ffprobeでMP4の情報を取得する。

    Args:
        video_path: MP4ファイルパス

    Returns:
        VideoInfo

    Raises:
        FileNotFoundError: ファイルまたはffprobeが見つからない場合
        RuntimeError: ffprobeが失敗した場合
    """
    if not video_path.exists():
        raise FileNotFoundError(f"Video file not found: {video_path}")

    ffprobe = shutil.which("ffprobe")
    if ffprobe is None:
        raise FileNotFoundError("ffprobe not found (install ffmpeg)")

    result = subprocess.run(
        [
            ffprobe,
            "-v",
            "quiet",
            "-print_format",
            "json",
            "-show_format",
            "-show_streams",
            str(video_path),
        ],
        capture_output=True,
        text=True,
        timeout=30,
    )

    if result.returncode != 0:
        raise RuntimeError(f"ffprobe failed: {result.stderr}")

    data = json.loads(result.stdout)
    fmt = data["format"]
    streams = data["streams"]

    video_stream = None
    audio_stream = None
    for s in streams:
        if s["codec_type"] == "video" and video_stream is None:
            video_stream = s
        elif s["codec_type"] == "audio" and audio_stream is None:
            audio_stream = s

    if video_stream is None:
        raise RuntimeError("No video stream found in file")

    return VideoInfo(
        duration=float(fmt["duration"]),
        width=int(video_stream["width"]),
        height=int(video_stream["height"]),
        video_codec=video_stream["codec_name"],
        audio_codec=(
            audio_stream["codec_name"] if audio_stream else None
        ),
        audio_sample_rate=(
            int(audio_stream["sample_rate"]) if audio_stream else None
        ),
        file_size=int(fmt["size"]),
        nb_streams=int(fmt["nb_streams"]),
    )


def validate_video(
    video_path: Path,
    *,
    expected_duration: float | None = None,
    duration_tolerance: float = _DURATION_TOLERANCE,
) -> list[str]:
    """生成されたMP4動画をバリデーションする。

    チェック項目:
    1. ファイルが存在し、サイズ > 0
    2. ffprobeで読み取れる（破損していない）
    3. video streamが存在する（H.264）
    4. 解像度が偶数（libx264互換）
    5. 期待デュレーションとの乖離チェック（指定時）

    Args:
        video_path: MP4ファイルパス
        expected_duration: 期待されるデュレーション（秒）。Noneならスキップ
        duration_tolerance: デュレーション許容誤差（秒）

    Returns:
        エラーメッセージのリスト（空なら合格）
    """
    errors: list[str] = []

    # 1. ファイル存在・サイズ
    if not video_path.exists():
        return [f"File not found: {video_path}"]

    if video_path.stat().st_size == 0:
        return [f"File is empty: {video_path}"]

    # 2. ffprobe 読み取り
    try:
        info = probe_video(video_path)
    except (RuntimeError, FileNotFoundError, json.JSONDecodeError, KeyError) as e:
        return [f"Cannot read video: {e}"]

    # 3. コーデック
    if info.video_codec != "h264":
        errors.append(
            f"Unexpected video codec: {info.video_codec} (expected h264)"
        )

    if info.audio_codec is not None and info.audio_codec != "aac":
        errors.append(
            f"Unexpected audio codec: {info.audio_codec} (expected aac)"
        )

    # 4. 偶数解像度
    if info.width % 2 != 0:
        errors.append(f"Odd width: {info.width}")

    if info.height % 2 != 0:
        errors.append(f"Odd height: {info.height}")

    # 5. デュレーション
    if info.duration <= 0:
        errors.append(f"Invalid duration: {info.duration}")

    if expected_duration is not None:
        diff = abs(info.duration - expected_duration)
        if diff > duration_tolerance:
            errors.append(
                f"Duration mismatch: {info.duration:.1f}s "
                f"(expected {expected_duration:.1f}s "
                f"+/- {duration_tolerance:.1f}s)"
            )

    return errors
